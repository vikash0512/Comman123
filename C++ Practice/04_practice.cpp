#include<iostream>

using namespace std;

int main()
{
    int num1, num2;
    cout<<"Enter num1\n"; //is called insertion operator
    cin>>num1; //is called extraction operator

    cout<<"Enter num2\n"; //is called insertion operator
    cin>>num2; //is called extraction operator

    cout<<"The sum is: "<<num1+num2;
    
    return 0;
}
